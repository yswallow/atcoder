def n_s(num)
	if num >= 26
		{ char: "z" , num: num-26}
	end
	
	case num
	when 1
	{ char: "a", num: 0 }
when 2
	{ char: "b", num: 0 }
when 3
	{ char: "c", num: 0 }
when 4
	{ char: "d", num: 0 }
when 5
	{ char: "e", num: 0 }
when 6
	{ char: "f", num: 0 }
when 7
	{ char: "g", num: 0 }
when 8
	{ char: "h", num: 0 }
when 9
	{ char: "i", num: 0 }
when 10
	{ char: "j", num: 0 }
when 11
	{ char: "k", num: 0 }
when 12
	{ char: "l", num: 0 }
when 13
	{ char: "m", num: 0 }
when 14
	{ char: "n", num: 0 }
when 15
	{ char: "o", num: 0 }
when 16
	{ char: "p", num: 0 }
when 17
	{ char: "q", num: 0 }
when 18
	{ char: "r", num: 0 }
when 19
	{ char: "s", num: 0 }
when 20
	{ char: "t", num: 0 }
when 21
	{ char: "u", num: 0 }
when 22
	{ char: "v", num: 0 }
when 23
	{ char: "w", num: 0 }
when 24
	{ char: "x", num: 0 }
when 25
	{ char: "y", num: 0 }
when 26
	{ char: "z", num: 0 }
else
	{ char: "z" , num: num-26}
end
end
